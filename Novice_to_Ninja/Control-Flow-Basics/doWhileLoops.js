// Sometimes you want the code to run once, even if the condition is not true
// Do While Loop is an extension of the While Loop

let i = 5;
// do this code and it's going to do it once to begin with, regardless of the "condition: i < 5" being true or false
do {
  console.log("val of i is: ", i);
  i++;
} while (i < 5);
